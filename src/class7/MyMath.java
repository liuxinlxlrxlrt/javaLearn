package class7;
/*
 * 定义一个类MyMath,提供基本的加减乘除功能，然后进行测试
 */

public class MyMath {
	//加法功能
	public int add(int a,int b) {
		return a+b;
	}
	//减法功能
	public int sub(int a,int b) {
		return a-b;
	}
	//乘法功能
	public int mul(int a,int b) {
		return a*b;
	}
	//除法功能
	public int div(int a,int b) {
		return a/b;
	}
}
