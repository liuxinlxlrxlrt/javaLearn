package class16_List_LinkedList;

/*
 * 需求：去除集合中自定义对象的重复值(对象的成员变量值都相同)
 * 
 * 我们按照和字符串一样的操作，发现出问题了。
 * 为什么呢？
 * 		我们必须思考哪里会出问题？
 * 		通过简单的分析，我们知道了问题出现在了判断上
 * 		而这个判断功能是集合自己提供的，所以我们如果想很清楚的知道它是如何判断的，我就应该去看源码
 * contains()方法的底层依赖的是equals()方法。
 * 而我们的学生类中没有equals()方法，这个时候，默认使用的是它父亲Object的equals()方法
 * 而Object的equals()方法默认比较的是地址值，所以他们都进去了，因为它new的东西地址值都不相同
 * 按照我们自己的需求，比较成员变量的值，重写equals()方法即可。
 * 即自动生成即可。
 */
import java.util.ArrayList;
import java.util.Iterator;
import class15_Collection.Student;

public class ArrayListDemo5 {
	public static void main(String[] args) {
		// 创建集合对象
		ArrayList array = new ArrayList();

		// 创建学生对象
		Student s1 = new Student("李嘉欣", 27);
		Student s2 = new Student("关之琳", 43);
		Student s3 = new Student("赵韩樱子", 25);
		Student s4 = new Student("刘诗诗", 18);
		Student s5 = new Student("刘亦菲", 16);
		Student s6 = new Student("李嘉欣", 27);
		Student s7 = new Student("李嘉欣", 18);

		// 添加元素
		array.add(s1);
		array.add(s2);
		array.add(s3);
		array.add(s4);
		array.add(s5);
		array.add(s6);
		array.add(s7);

		// 创建新集合
		ArrayList newArray = new ArrayList();

		// 遍历旧集合，获取每一个元素
		Iterator it = array.iterator();
		while (it.hasNext()) {
			Student s = (Student) it.next();
			if (!newArray.contains(s)) {
				newArray.add(s);
			}
		}

		System.out.println("----------------------");

		// 遍历
		for (int x = 0; x < newArray.size(); x++) {
			Student s = (Student) newArray.get(x);
			System.out.println(s.getName() + "---" + s.getAge());
		}
	}
}
