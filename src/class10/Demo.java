package class10;
/*
 * 修饰符：
 * 		权限修饰符:
 * 			private,默认的,protected,public
 * 		状态修饰符：
 * 			static,final
 * 		抽象修饰符：
 * 			abstract
 * 
 * 类：
 * 		权限修饰符:
 * 			默认的,public
 * 		状态修饰符：
 * 			final
 * 		抽象修饰符：
 * 			abstract
 * 
 * 		用的最多的就是：public
 * 
 * 成员变量：
 * 		权限修饰符:
 * 			private,默认的,protected,public
 * 		状态修饰符：
 * 			static,final
 * 
 * 		用的最多的是：private
 * 
 * 	构造方法：
 * 		 权限修饰符:
 * 			private,默认的,protected,public
 * 		状态修饰符：
 * 			static,final
 *  	用的最多的是public
 *  
 *  成员方法：
 * 		权限修饰符:
 * 			private,默认的,protected,public
 * 		状态修饰符：
 * 			static,final
 * 		抽象修饰符：
 * 			abstract
 * 
* 		用的最多的就是：public
* 
* 除此以外的组合规则：
* 		成员变量：public static final
* 		成员方法：public static 
* 				 public abstract
* 				 public final
 */
//此处不允许使用修饰符private
//此处不允许使用修饰符protected
