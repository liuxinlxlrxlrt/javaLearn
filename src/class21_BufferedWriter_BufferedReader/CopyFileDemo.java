package class21_BufferedWriter_BufferedReader;
import java.io.BufferedReader;
import java.io.BufferedWriter;
import java.io.FileNotFoundException;
import java.io.FileReader;
import java.io.FileWriter;
import java.io.IOException;

/*
 * * 需求：把当前项目目录下的a.txt内容复制到当前项目目录下的d.txt中
 * 
 * 数据源：
 * 	a.txt--读取数据--转换字符流--InputStreamReader--FileReader--BufferedReader
 * 
 * 目的地：
 * 	d.txt--写出数据--转换字符流--OutputStreamWriter--FileWriter--BufferedWriter
 */
public class CopyFileDemo {

	public static void main(String[] args) throws IOException {
		//封装数据源
		BufferedReader br = new BufferedReader(new FileReader("a.txt"));
		//封装目的地
		BufferedWriter bw = new BufferedWriter(new FileWriter("d.txt"));
		
		//两种方式其中的一种一次读取一个字符数组
		char[] chs = new char[1024];
		int len = 0;
		while((len = br.read(chs))!=-1) {
			bw.write(chs, 0, len);
			bw.flush();
		}
		
		bw.close();
		br.close();
		
	}

}
